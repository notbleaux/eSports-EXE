"""[Ver001.000]
Generate Chaos Engineering Report

This script generates a comprehensive report from chaos experiment results.
Can be run standalone or as part of CI/CD pipeline.

Usage:
    python scripts/generate_chaos_report.py [--output-dir reports/]
"""

import argparse
import json
import sys
from datetime import datetime, timezone
from pathlib import Path
from typing import Any, Dict, List


def load_test_results(test_output_file: Path) -> Dict[str, Any]:
    """Load pytest test results from JSON file."""
    if not test_output_file.exists():
        return {}
    
    try:
        return json.loads(test_output_file.read_text())
    except (json.JSONDecodeError, IOError):
        return {}


