def main() -> int:
    """Main entry point."""
    parser = argparse.ArgumentParser(
        description="Generate Chaos Engineering Report"
    )
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=Path("reports"),
        help="Output directory for report"
    )
    parser.add_argument(
        "--test-results",
        type=Path,
        help="Pytest JSON results file"
    )
    
    args = parser.parse_args()
    
    # Create output directory
    args.output_dir.mkdir(parents=True, exist_ok=True)
    
    # Load test results if provided
    results = {}
    if args.test_results:
        results = load_test_results(args.test_results)
    
    # Generate report
    timestamp = datetime.now(timezone.utc).strftime("%Y%m%d-%H%M%S")
    output_path = args.output_dir / f"chaos-report-{timestamp}.html"
    
    generate_html_report(results, output_path)
    
    return 0


if __name__ == "__main__":
    sys.exit(main())
